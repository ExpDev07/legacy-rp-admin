        <div class="content-box collapse multi-collapse" id="criminal_rules">
            <h1 class="display-4">Criminal Roleplay</h1>
            <p><strong>(7.1)</strong> When taking hostages, you must provide high-quality roleplay.</p>
            <p><strong>(7.2)</strong> You are not allowed to use your friends or gang members as hostages. Hostages
                must be players that have no idea what you are planning.</p>
            <p><strong>(7.3)</strong> You are not allowed to use your phone to warn the police. Messages sent over the
                phone such as: “we will shoot any cops within 10 kilometers” will not be accepted.</p>
            <p><strong>(7.4)</strong> When the police turn their sirens’ lights on to pull you over, roleplay is not
                initiated. This means that you cannot start shooting at police just for attempting to pull you over.
                Such actions will be classed as clear RDM.</p>
            <p><strong>(7.5)</strong> Any person wearing a mask is unidentifiable. This means that you should not
                remember who they are if they, for example, rob you. An exception to this is if they are wearing
                distinguishable gang clothing or have visible gang tattoos, in which case you can identify them by the
                gang but not individual people.</p>
            <p><strong>(7.6)</strong> The maximum amount of money you can request for a hostage is $100,000 if they are
                an officer or $50,000 otherwise.</p>
            <p><strong>(7.7)</strong> When robbing someone, all you can take is currently what’s in their inventory.
                This means that you cannot request bank transfers or force them to sell their car(s) for you. Neither
                are you allowed to take items from people’s apartment.</p>
            <p><strong>(7.8)</strong> For an execution to occur, there must be: 1. high-quality roleplay involved, and
                2. a very good reason for it to take place.</p>
