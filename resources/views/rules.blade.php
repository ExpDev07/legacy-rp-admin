            <p><strong>(6.3)</strong> You must, under no circumstances, kill anyone in the Green Zone.</p>
            <p><strong>(6.4)</strong> You must not perform robberies or attempt to engage in violent/hostile roleplay.
            </p>
            <p><strong>(6.5)</strong> You cannot steal any vehicles in the Green Zone.</p>
            <p><strong>(6.6)</strong> You are not to engage in illegal activities such as selling drugs or weapons.</p>
            <p><strong>(6.7)</strong> Law enforcement (police) may carry out their duties in the Green Zone even if
