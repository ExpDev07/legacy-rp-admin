            <h1 class="display-4">Heist Rules</h1>
            <p><span class="lead">Heists can be a fun way to make money on our servers.</span></p>
            <p><strong>(8.1) Bank heists</strong><br/></p>
            <p><span class="lead">The banks are where the citizens’ money is stored. The money is insured, so the citizens will be left
                unaffected. Robbing a bank is a big operation and requires a lot of planning. To ensure high-quality
                    roleplay, we have set some strict rules.</span></p>
            <p><strong>(8.1.1)</strong> At least 5 police officers must be online for a bank heist to take place. We
                have a system in place which prohibits you from starting a robbery unless the required amount of cops
                are online.</p>
            <p><strong>(8.1.2)</strong> The police will always send a negotiator to talk to the robbers. You then have
                two options: start roleplaying with the negotiator or decline by asking him/her to leave. You may not
                shoot the negotiator. You may also not kidnap this person.</p>
            <p><strong>(8.1.3)</strong> Only 7 people, excluding any hostage(s), may actively participate in a bank
                robbery.</p>
            <p><strong>(8.2) Store robberies</strong></p>
            <p><span class="lead">Convenience stores are great to rob if you want to get your hands on some quick cash without the risks</span><span>that come with robbing a bank.</span>
            </p>
            <p><strong>(8.2.1)</strong> Only 4 people, excluding any hostage(s), may actively participate in a store
                robbery.</p>
            <p><strong>(8.3)</strong> Negotiations are considered over if a shot is fired or if the police deem it
                necessary for immediate intervention. At this point, police are allowed to storm the building with armed
                forces and any combat, as a result, will not be considered RDM.</p>
