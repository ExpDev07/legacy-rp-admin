            <h1 class="display-4">Police Rules</h1>
            <p class="lead">Being a police officer at Legacy Roleplay is a huge privilege. It allows you to engage in some very
                interesting and awesome roleplay scenarios. In addition, you get to enforce the law! However, to ensure
                high-quality roleplay from the police, we have set a few rules.</p>
            <p><strong>(9.1)</strong> All police must have a working microphone and be available on discord.</p>
            <p><strong>(9.2)</strong> Police equipment (weapons, items, etc) are not to be exchanged with players
                outside of the force. This is only acceptable if there is solid roleplay behind it.</p>
            <p><strong>(9.3)</strong> Tasers (non-lethal force) should be used unless there is a direct danger to
                surrounding officers or civilians or otherwise instructed by a high-ranking officer. Otherwise, lethal
                force can be used to terminate the danger.</p>
            <p><strong>(9.4)</strong> The police role should not be abused to help friends.</p>
            <p><strong>(9.5)</strong> When going off-duty as an officer, you are to dispose of any equipment and
                vehicles that you got from the police department. You may also not get equipment from the PD while
                off-duty.</p>
            <p><strong>(9.6)</strong> Negotiators are not to be taken as a hostage. If you find yourself in such a
                situation, make sure to report it.</p>
            <p><strong>(9.7)</strong> Police can revive people if no EMS is currently online.</p>
            <p><strong>(9.8)</strong> Police cannot perform executions unless under rare circumstances. The execution
