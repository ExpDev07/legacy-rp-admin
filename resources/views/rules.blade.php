            <h1 class="display-4">Metagaming Rules</h1>
            <p><strong>(4.1)</strong> Using intelligence which was not obtained through roleplay to influence actions
                in-game is considered metagaming and is not allowed.</p>
            <p><strong>(4.2)</strong> You are not allowed to identify people by using their assigned IDs. This number
                is only to be used when you wish to report somebody or by staff when performing certain actions on
                players (such as kicking). Punishment is a kick or depending on the context, may lead to a more strict
                punishment.</p>
            <p><strong>(4.3)</strong> If you are restrained or if your communication devices have been removed then you
                must make yourself inactive on any other forms of communication besides proximity chat.</p>
            <p><strong>(4.4)</strong> Stream sniping is considered metagaming and is not allowed. If a player is
                streaming on our servers, then you must not use the information that you gain from watching the stream
                in-game.</p>
