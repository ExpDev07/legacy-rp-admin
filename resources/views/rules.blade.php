        <div class="content-box collapse multi-collapse" id="roleplay_rules">
            <h1 class="display-4">Roleplay Rules</h1>
            <p><strong>(3.1)</strong> Powergaming is strictly prohibited. This is when you aim to maximize progress
                towards a specific goal, to the exclusion of other considerations such as storytelling (roleplay) and
                atmosphere.</p>
            <p><strong>(3.2)</strong> FailRP - is the act of failing to engage in roleplay. When playing, you must
                always be engaging in roleplay.</p>
            <p><strong>(3.3)</strong> You must remain in-character always unless a staff member authorizes you to speak
                out of character by declaring a staff situation. We value roleplay over everything.</p>
            <p><strong>(3.4)</strong> All OOC (Out of Character) interactions must be made through the “OOC Proximity”
                chat channel. You can use this by typing the command: "/oocp ".</p>
            <p><strong>(3.5)</strong> You are to always have and be able to use a microphone while playing on our
                servers.</p>
            <p><strong>(3.6)</strong> You must hot-mic all communication that is made outside of the server’s voice
                channel if the it is relevant to the roleplay. This means, for example, that if you are communicating
                with a fellow team member on Discord or Teamspeak and you are within the audio proximity of another
                player (they can hear you), then you must ensure that player can overhear everything that you are
                saying. Note that this rule does not apply if you are in a direct gunfight or if you are in no roleplay
                situation at all.</p>
            <p><strong>(3.6.1)</strong> When your communication devices have been stripped from you, then all
                communication is to be made through proximity/direct [voice] chat.</p>
            <p><strong>(3.7)</strong> Continuing to roleplay a character of a sensitive nature after being requested to
                stop is punishable by a ban. A sensitive character could, for example, be Adolf Hitler or Joseph Stalin.
            </p>
            <p><strong>(3.8)</strong> Baiting is not allowed. Baiting for the sake of a gunfight is not considered
                roleplay. A few examples of baiting are: doing donuts around the police to provoke a reaction, robbing a
                gas station only to start a gunfight, and standing on top of somebody’s car without any RP to get a
                reaction from the owner.</p>
            <p><strong>(3.9)</strong> After a server restart, all roleplay that happened within 15 minutes before
                should be forgotten.</p>
            <p><strong>(3.10)</strong> Roleplaying as a nude character is not allowed. An exception for this rule is if
                you are inside the strip club (Vanilla Unicorn).</p>
            <p><strong>(3.11)</strong> Playing as a sexist character is not tolerated at our servers regardless of how
                good your roleplay is. Punishment is a permanent ban.</p>
            <p><strong>(3.12)</strong> We recommend using "/me " when performing certain actions that greatly impacts
                the roleplay situation, as it greatly improves the quality and imersiveness of the roleplay.</p>
            <p><strong>(3.13)</strong> You are not allowed to “ass pull”. GTA’s inventory system allows you to pull out
                a big assault rifle from your pocket in half a second. This is unrealistic, so we have set a rule to
                prohibit this. Weapons not listed in 3.15 must be roleplayed by using “/me” to signalize the action of
                drawing the weapon. These weapons must be either drawn from a bag or a vehicle (police often do this).
            </p>
            <p><strong>(3.14)</strong> You may not use weapons not listed in 3.15 to blindfire when in cover.</p>
            <p><strong>(3.15)</strong> Pistols, mini/micro-SMG, one-handed melee weapons, and tec-9.</p>
            <p><strong>(3.16)</strong> OOC channels should not be used to resolve roleplay situations. This means that
                you should not use these channels to tell people that x and y are not allowed nor threaten to file
                reports. Of course, you may do this on before-hand, but not after actions have already been committed
                and/or roleplay is completed.</p>
