            <h1 class="display-4">General Rules</h1>
            <p><strong>(2.1.1)</strong> VDM (Vehicle Deathmatch) is strictly prohibited. At Legacy Roleplay, we define
                VDM as the act or effort of intentionally using a vehicle to kill or injure other persons, or, where a
                player kills another person, without intent, by reckless performance under circumstances where any sane
                individual would understand that such performance could beyond reasonable doubt result in the death of
                another person. Examples of VDM include, but are not limited to: intentionally running people over or
                causing explosions to vehicles in order to kill or injure any person(s), speeding excessively with a car
                hitting a motorcycle where the rider/passenger dies or causing an explosion to a vehicle in a high
                traffic area like Legion Square which results in the death of any person(s).</p>
            <p><strong>(2.1.2)</strong> An act in which a person uses his vehicle as a weapon in order to protect his
                own life from any direct or immediate threat, will not be considered VDM. A direct or immediate threat
                in this section is defined as any individual who actively attempts to take your life in the moment. If a
                person who attempts to kill you, but falls short of completing the act, tries to flee the scene, he is
                no longer considered a direct threat to your life.</p>
            <p><strong>(2.2</strong>) Random Deathmatch (also known as RDM) is the act of killing/downing somebody
                without first engaging in quality roleplay. The mentioned roleplay should always include directly
                initiating on somebody’s life or having your own life initiated on. You are also to give people an
                incentive and enough time to comply with your orders (e.g during a robbery, do not shoot after 3 seconds
                if they don’t listen). Note that we do not consider quick countdowns quality roleplay.</p>
            <p><strong>(2.3)</strong> Fear RP is not allowed. This means that you are to value your life at all times
                and act accordingly in situations. For example, if you have a gun pointed at you, then you are to listen
                to commands given by the person holding the gun or anyone affiliated with that person to ensure your own
                survival.</p>
            <p><strong>(2.4)</strong> Any modifications that change the functionality of the game or changes how our
                systems/scripts work (also known as modding and scripting) is strictly prohibited on our servers and
                will result in a permanent ban being issued. An example of a modification would be a crosshair or one
                that gives you free money.</p>
            <p><strong>(2.5)</strong> Bugs are inevitable as the server is in constant development. Exploiting any of
                these in-game bugs and using them to your advantage is not allowed. If you discover a bug that can be
                exploited, you are to immediately report it to any staff member available and they will make sure it’s
                taken care of. You are also not to exploit certain game mechanics such as using the deposit/withdraw for
                your car to repair it.</p>
            <p><strong>(2.6)</strong> Combat logging during any roleplay situation is considered exploitation. Some
                examples of this would be: logging out during a firefight, being chased by the police, arrested by the
                police, while dead awaiting a medic, logging out to save gear, rage-quitting, logging out whilst
                restrained/detained, etc. We are aware of common crashes, connection issues, etc, however, this is often
                reflected in the logs and staff will make an informed decision on whether you’ve tried to resolve the
                issue by, for example, logging back on.</p>
            <p><strong>(2.7)</strong> Trolling is the deliberate act of performing certain actions to provoke a
                reaction. We have a very clear list of what we consider trolling, but we advise using common sense as
                trolling is a very broad subject, and it is impossible to list everything. We consider the following
                trolling: running around while people are trying to roleplay, throwing yourself in front of vehicles,
                blocking people with your car without RP, jumping off buildings, bombarding people with messages, and
                playing soundboards/voice changers in-game without RP and in our discord channels. Depending on the
                severity, a violation of this rule could lead to a permanent ban without appeal.</p>
            <p><strong>(2.8</strong>) Breaking roleplay in-voice (also known as going Out of Character) is never
                allowed unless a staff member arrives and clearly announces that there now is an on-going staff
                situation. We have seen so many cases of people having personal arguments and threating to file reports,
                and we want to make clear that this is not allowed.</p>
            <p><strong>(2.9)</strong> Trading real-life items for any in-game luxury is not allowed. This is because we
                do not want to be seen as a commercial entity as we feel it is unethical and FiveM strictly prohibits
                it. Donations made to cover server costs is an exemption.</p>
            <p><strong>(2.10</strong>) We have long queues (full slots) at times due to a very active and engaging
                community. Therefore, we ask for you to kindly disconnect from the server to let other people in if you
                have other important business to attend for a few minutes.</p>
            <p><strong>(2.11)</strong> Playing on the side-server is a bannable offense. When you log in, it can happen
                that you are sent to the “side-server”. This is a place where you are unable to see or render anyone.
                You are to leave immediately and reconnect.</p>
            <p><strong>(2.12)</strong> You are not to steal any helicopters if you find one. They are exclusive for
                some whitelisted jobs.</p>
            <p><strong>(2.13)</strong> Driving at the airstrip inside the international airport is considered Fail RP.
                You are also not to steal any vehicles that can found at the airport. Note that “airport” only refers to
                the area behind the gates leading to the airstrip.</p>
            <p><strong>(2.14)</strong> To ensure that the server’s performance keeps steady, we ask of you to wait for
                the vehicle to load at the car dealership before you proceed to look at the next one.</p>
            <p><strong>(2.15)</strong> When doing upgrades on your vehicle, make sure to deposit it to the garage and
                then withdraw it again to make the customizations persist over server restarts. There will be no refunds
                given for this.</p>
            <p><strong>(2.16)</strong> Presenting yourself as a person with the ability to clean money, and then
                scamming people for their dirty cash is not allowed.</p>
            <p><strong>(2.17)</strong> Stealing emergency vehicles is strictly prohibited unless you have a valid
                roleplay reason for it.</p>
            <p><strong>(2.18)</strong> Using your phone to call jobs in order for them to arrive only so you can rob,
                take them as a hostage, or perform any other illegal activities will be punished accordingly.</p>
            <p><strong>(2.19)</strong> When playing, it is important to keep to realistic capabilities. This includes
