            <h1 class="display-4">Newlife Rules</h1>
            <p><strong>(5.1)</strong> When a new life starts, your character will forget all the information and
                events leading up to your death. However, you are still the same character (same job, gang,
                friends).</p>
            <p><strong>(5.2)</strong> A new life starts, when:</p>
            <ul>
                <li>You are executed (see 7.10).</li>
                <li>You re-spawn.</li>
            </ul>
            <p><strong>(5.3)</strong> A new life does not start when:</p>
            <ul>
                <li>Your character has been the victim of a clear case of VDM</li>
                <li>Your character has been the victim of a clear case of RDM</li>
                <li>You are downed (awaiting reviving)</li>
                <li>You disconnect (end of the session)</li>
            </ul>
            <p><strong>(5.4)</strong> When you re-spawn:</p>
            <ul>
                <li>Your character cannot return within 500m of the location of death or be involved in the same
                    combat situation until the situation is over or 10 minutes have passed.
                </li>
            </ul>
            <strong>(5.5)</strong> When you are revived:
            <ul>
                <li>You may not actively engage in or continue the same combat situation for the next 10 minutes.
                </li>
                <li>Your character remembers all the information before they were downed.</li>
            </ul>
            <strong>(5.6)</strong> When you are downed:
            <ul>
                <li>You cannot communicate with anyone while downed, especially not give any tactical information
                    to your teammates.
                </li>
                <li>You are supposed to be hurt. Do not talk unless you were downed by something that isn’t
                    critical, but if you do decide to talk, at least act hurt.
                </li>
            </ul>

